'''
Função para encontrar a subtração de dois números

- O nome da função deve ser subnumbers()
- Deve calcular as diferenças entre dois números
'''
# function for subtraction of 2 numbers
def subNumbers(x, y):
    return (x - y);

# main code
a = 90;
b = 50;

# finding subtraction
res = subNumbers(a, b)

# print statement
print(f"subtraction of {a} and {b} is {res}");